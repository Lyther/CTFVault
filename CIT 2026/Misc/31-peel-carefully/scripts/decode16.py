import collections

codon_table = {
    'GGA': 'G', 'GGC': 'G', 'GGG': 'G', 'GGT': 'G',
    'GTA': 'V', 'GTC': 'V',
    'TCG': 'S', 'TCT': 'S',
    'TGA': '*', 'TGC': 'C', 'TGG': 'W', 'TGT': 'C',
    'TTA': 'L', 'TTC': 'F', 'TTG': 'L', 'TTT': 'F'
}

with open("/Users/bytedance/Documents/CTF/CIT 2026/Misc/31-peel-carefully/files/layer1.txt", "r") as f:
    data = f.read().strip()

triplets = [data[i:i+3] for i in range(0, len(data), 3)]
chunks = []
curr = []
for t in triplets:
    if t == "ATG":
        if curr: chunks.append(curr)
        curr = []
    else:
        curr.append(t)
if curr: chunks.append(curr)

aa_pairs = []
for c in chunks:
    if len(c) == 2:
        aa_pairs.append(codon_table[c[0]] + codon_table[c[1]])

# Let's map the 29 unique AA pairs to ASCII characters based on frequency
# and see if it looks like English.
unique_pairs = list(set(aa_pairs))
print("Unique pairs:", len(unique_pairs))
print(collections.Counter(aa_pairs).most_common())
