    for c2 in unique_clusters:
        if c1 != c2:
            stack = 0
            valid = True
            for c in clusters:
                if c == c1:
                    stack += 1
                elif c == c2:
                    stack -= 1
                    if stack < 0:
                        valid = False
                        break
            if valid:
                print(
                    f"Valid prefix pair: {c1} as [, {c2} as ] (unmatched [ at end: {stack})",
                )
